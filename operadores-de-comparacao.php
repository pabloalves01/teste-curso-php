//Operadores de Comparação
// == Igual. ----- ex: 10 == 10 ----- True (Funciona com Int e String)
// != Não igual ----- ex: 10 != 10 ----- False
// === Identico ----- ex: 10 === 10 ----- True (Não funciona com Int e String)
// !=== Não é identico ----- ex: 10 !== "10" ----- False (Não é identico)
// <> Diferente ----- ex: 10 <> 11 ---- True (10 é diferente de 11)
// > Maior que ----- ex: 10 > 9 ---- True (10 é maior que 9)
// < Menor que ----- ex: 10 < 9 ----- False (10 não é menor que 9)
// <= Menor ou igual ----- ex: 10 <= 10 ----- True (10 é menor ou igual a 10)
// >= Maior ou igual ----- ex: 10 >= 10 ----- True (10 é maior ou igual a 10)
// <=> Comparação combinada ----- ex: 20 <=> 30 = -1 (Se o lado esquerdo for menor ele retorna -1)

if(10 == 10)
{
    echo "True";
}
else
{
    echo "False";
}

